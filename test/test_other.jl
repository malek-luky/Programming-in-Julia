# Speed Tests
p = Polynomial( convert(Vector{Float32}, (4 * (rand(100) .- 0.5))))
q = Polynomial( convert(Vector{Float32}, 4 * (rand(8) .- 0.5)))

@testset "$(rpad("Even more custom examples", TITLE_WIDTH))" begin
    # sum
    @time @test p+q == p+q
    #Substraction
    @time @test p-q == p-q
    #Multiplication
    @time @test p*q == p*q

    # pdiv
    quotient, remainder = pdiv(p, q)
    @time @test p == (quotient * q) + remainder
end